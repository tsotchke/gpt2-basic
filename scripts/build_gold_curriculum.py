def clean_ascii(text: str) -> str:
    text = text.encode("ascii", errors="ignore").decode("ascii")
    text = text.replace("\r\n", "\n").replace("\r", "\n")
    text = re.sub(r"\s+", " ", text)
    return "".join(ch for ch in text if 32 <= ord(ch) <= 126).strip()


def topic_answer(topic: Topic, offset: int) -> str:
    fact_a = topic.facts[offset % len(topic.facts)]
    fact_b = topic.detail[offset % len(topic.detail)]
    contrast = topic.contrast[offset % len(topic.contrast)]
    return clean_ascii(f"{fact_a} {fact_b} {contrast}")


