def audit_doc(doc: str) -> list[str]:
    issues: list[str] = []
    if len(doc) < 80:
        issues.append("too_short")
    if len(doc) > 560:
        issues.append("too_long")
    for pattern in BANNED_PATTERNS:
        if re.search(pattern, doc, flags=re.IGNORECASE):
            issues.append("banned_pattern")
            break
    for pattern in CORRUPT_PATTERNS:
        if re.search(pattern, doc, flags=re.IGNORECASE):
            issues.append("corrupt_pattern")
            break
    alpha = sum(1 for ch in doc if ch.isalpha())
    if alpha / max(1, len(doc)) < 0.55:
        issues.append("low_alpha")
    if re.search(r"\s{2,}", doc):
        issues.append("extra_space")
    return issues


def token_distribution(docs: list[str]) -> tuple[dict[str, int], float, int, list[str]]:
    tokenizer = build_lexicon_tokenizer(docs, 4096, min_count=1, max_phrase_words=3)
    counts = {"lexicon": 0, "alpha_byte": 0, "byte": 0, "other": 0}
    for doc in docs:
        for token in tokenizer.encode(doc, output_safe=True):
            if BYTE_OFFSET <= token < BYTE_VOCAB_SIZE:
                byte = token - BYTE_OFFSET
                if chr(byte).isalpha():
                    counts["alpha_byte"] += 1
                else:
                    counts["byte"] += 1
            elif token >= BYTE_VOCAB_SIZE:
                counts["lexicon"] += 1
            else:
                counts["other"] += 1
    total = max(1, sum(counts.values()))
    alpha_byte_ratio = counts["alpha_byte"] / total
    top_pieces = [
        tokenizer.id_to_piece[token_id].decode("ascii", errors="ignore")
        for token_id in range(BYTE_VOCAB_SIZE, min(tokenizer.vocab_size, BYTE_VOCAB_SIZE + 80))
    ]
    return counts, alpha_byte_ratio, tokenizer.vocab_size, top_pieces


def display_path(path: Path) -> str:
    try:
        return str(path.relative_to(ROOT))
    except ValueError:
        return str(path)


def report_text(
    version: str,
    output: Path,
    docs: list[str],
    counts: dict[str, int],
    alpha_ratio: float,
    vocab_size: int,
    top_pieces: list[str],
) -> str:
    bad_docs = [(idx + 1, audit_doc(doc), doc) for idx, doc in enumerate(docs) if audit_doc(doc)]
    lines = [
        f"# GPT2-BASIC Gold Curriculum {version}",
        "",
        "This is a hand-curated narrow corpus for the large lexicon tokenizer path.",
        "It avoids report mining, scoring instructions, code paths, and previous malformed generations.",
        "",
        "## Summary",
        "",
        f"- Corpus: `{display_path(output)}`",
        f"- Documents: {len(docs)}",
        f"- Characters: {sum(len(doc) for doc in docs)}",
        f"- Audit failures: {len(bad_docs)}",
        f"- Probe lexicon vocab size: {vocab_size}",
        f"- Probe lexicon targets: {counts['lexicon']}",
        f"- Probe alphabetic byte targets: {counts['alpha_byte']}",
        f"- Probe alphabetic byte share: {alpha_ratio:.3f}",
        "",
        "## Top Probe Vocabulary Pieces",
        "",
    ]
    for piece in top_pieces:
        lines.append(f"- `{piece}`")
    if bad_docs:
        lines.extend(["", "## Audit Failures", ""])
        for idx, issues, doc in bad_docs[:20]:
            lines.append(f"- Document {idx}: {', '.join(issues)}: {doc[:160]}")
    return "\n".join(lines) + "\n"


def write_outputs(version: str, output: Path, report: Path) -> tuple[list[str], dict[str, int], float, int]:
    docs = build_gold_docs(version)
    failures = [(idx + 1, audit_doc(doc), doc) for idx, doc in enumerate(docs) if audit_doc(doc)]
    if failures:
        preview = "; ".join(f"{idx}:{','.join(issues)}" for idx, issues, _doc in failures[:10])
        raise ValueError(f"gold corpus audit failed: {preview}")
    counts, alpha_ratio, vocab_size, top_pieces = token_distribution(docs)
    if alpha_ratio > 0.10:
        raise ValueError(f"alphabetic byte target share too high: {alpha_ratio:.3f}")
    output.parent.mkdir(parents=True, exist_ok=True)
    report.parent.mkdir(parents=True, exist_ok=True)
    output.write_text("\n\n".join(docs) + "\n", encoding="ascii")
    report.write_text(report_text(version, output, docs, counts, alpha_ratio, vocab_size, top_pieces), encoding="ascii")
    return docs, counts, alpha_ratio, vocab_size


